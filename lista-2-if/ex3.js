var rl = require("readline-sync")



var n = rl.question("Digite o DDI  ")


if (n == 1) {
    console.log( "Estados Unidos" )
    
} else if (n == 49) {
    console.log( " Alemanha" )

} else if (n == 54) {
    console.log( "Argentina" )

} else if (n == 55) {
    console.log( "Brasil" )

} else if (n == 35) {
    console.log( "Portugal" )

} else {
    console.log( "O DDI nao esta cadastrado" )

}  

   
